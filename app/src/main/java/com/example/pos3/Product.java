package com.example.pos3;

public class Product {

    public int id;
    public String name, category;
    public double price;
    public int quantity;


    public Product (int id, String name, String category, double price, int quantity){
        this.id = id;
        this.name = name;
        this.category = category;
        this.price = price;
        this.quantity = quantity;
    }


}



